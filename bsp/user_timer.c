void TIM1_PWM_Use()
{
		u8 dir=1;	
	u16 led0pwmval=0; 
	TIM1_PWM_Init(899,0);//不分频。PWM频率=72000/(TopValue+1)=80Khz 
	while(1)
	{
 		//delay_ms(5);	 
		if(dir)led0pwmval++;
		else led0pwmval--;	 
 		if(led0pwmval>600)dir=0;
		if(led0pwmval==0)dir=1;	   					 
	  TIM_SetCompare1(TIM1,led0pwmval);	
	}
}
